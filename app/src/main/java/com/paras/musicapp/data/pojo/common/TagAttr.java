package com.paras.musicapp.data.pojo.common;

public class TagAttr {
    private String tag;
    private long page;
    private long perPage;
    private long totalPages;
    private long total;

    public String getTag() {
        return tag;
    }

    public long getPage() {
        return page;
    }

    public long getPerPage() {
        return perPage;
    }

    public long getTotalPages() {
        return totalPages;
    }

    public long getTotal() {
        return total;
    }
}
